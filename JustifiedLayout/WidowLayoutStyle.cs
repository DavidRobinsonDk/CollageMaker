namespace JustifiedLayout;

/// <summary>
/// Specifies the layout style for widow items in justified layout.
/// </summary>
public enum WidowLayoutStyle
{
    /// <summary>
    /// Align widow items to the left.
    /// </summary>
    Left,

    /// <summary>
    /// Justify widow items across the available width.
    /// </summary>
    Justify,

    /// <summary>
    /// Center widow items.
    /// </summary>
    Center
}
